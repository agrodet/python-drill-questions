def to_binary_str(num):
    digit_list = []
    while """①""" > 0:
        digit_list.append(str("""②"""))  # 要素を追加
        num = int("""③""")
    digit_list.reverse()  # 順番を逆転させる
    return ''.join(digit_list)


print(to_binary_str(7))
print(to_binary_str(11))
print(to_binary_str("""④"""))
print(to_binary_str(32))
